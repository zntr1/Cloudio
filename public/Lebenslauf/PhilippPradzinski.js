function collapseArea1() {
    var area = document.getElementById("area1");
    var button = document.getElementById("btnCollapseArea1");
    if (area.style.display == "none") {
        button.textContent = "-";
        area.style.display = "block";
    } else {
        button.textContent = "+";
        area.style.display = "none";
    }

}

